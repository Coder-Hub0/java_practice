package org.deep.core;

public class IfElseExample {

	public static void main(String... strings) {
		// defining a variable
		int number = 13;
		// check if the number is divisible by 2 or not
		if (number % 2 == 0) {
			System.out.println("even Number");
		} else {
			System.out.println("odd Number");
		}

	}
}
